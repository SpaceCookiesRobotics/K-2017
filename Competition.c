#pragma config(Motor,  port3,           frontright,    tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port4,           backright,     tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port5,           backleft,      tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port6,           lift,          tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port7,           dr4b1,         tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port8,           dr4b2,         tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port9,           dr4b3,         tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port10,          dr4b4,         tmotorVex393_HBridge, openLoop)
