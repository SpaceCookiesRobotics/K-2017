#pragma config(Motor,  port1,           middleleft,    tmotorVex393_HBridge, openLoop)
#pragma config(Motor,  port2,           twoleft,       tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port3,           tworight,      tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port4,           claw,          tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port5,           lift,          tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port6,           dr4b1,         tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port7,           dr4b2,         tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port8,           dr4b3,         tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port9,           dr4b4,         tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port10,          middleright,   tmotorVex393_HBridge, openLoop, reversed)
//*!!Code automatically generated by 'ROBOTC' configuration wizard               !!*//

task main () {
	int joy_x;            // will hold the X value of the analog stick (choices below)
	int joy_y;            // will hold the Y value of the analog stick (choices below)
	int slowbutton = vexRT[Btn5U];       // either 0 or 1 (pressed or not pressed
	int slowdown;
	int threshold = 5;   // helps to eliminate extra 'noise' from a joystick that isn't perfectly at (0,0)

	while(1 == 1) {
		joy_x = vexRT[Ch4];   // This is the left analog stick.
		joy_y = vexRT[Ch3];
		if (slowbutton == 0) {  // If slowbutton is pressed, speed decreases by 5. If not, it stays the same.
			slowdown = 1; }
		if (slowbutton == 1) {
			slowdown = 5; }
		// Forward, and swing turns: (both abs(X) and abs(Y) are above the threshold, and Y is POSITIVE)
		if((abs(joy_x) > threshold) && (abs(joy_y) > threshold) && (joy_y > 0)){
			motor[twoleft]  = (joy_y + joy_x)/slowdown;
			motor[middleleft]  = (joy_y + joy_x)/slowdown;
			motor[tworight] = (joy_y - joy_x)/slowdown;
			motor[middleright] = (joy_y - joy_x)/slowdown;}
		// Backwards and swing turns: (both abs(X) and abs(Y) are above the threshold, and Y is NEGATIVE)
		else if((abs(joy_x) > threshold) && (abs(joy_y) > threshold) && (joy_y < 0)){
			motor[twoleft]  = (joy_y - joy_x)/slowdown;
			motor[middleleft]  = (joy_y - joy_x)/slowdown;
			motor[tworight]  = (joy_y + joy_x)/slowdown;
			motor[middleright] = (joy_y + joy_x)/slowdown;}
		// Turning in place to the right: (abs(X) is above the threshold, abs(Y) is below the threshold)
		else if((abs(joy_x) > threshold) && (abs(joy_y) < threshold)){
			motor[twoleft]  = (joy_x)/slowdown;
			motor[middleleft] = (joy_x)/slowdown;
			motor[tworight] = (-1 * joy_x)/slowdown;
			motor[middleright] = (-1 * joy_x)/slowdown;}
		// Turning in place to the right: (abs(Y) is above the threshold, abs(X) is below the threshold)
		else if((abs(joy_x) < threshold) && (abs(joy_y) > threshold)){
			motor[twoleft]  = (-1 * joy_x)/slowdown;
			motor[middleleft] = (-1 * joy_x)/slowdown;
			motor[tworight] = (joy_x)/slowdown;
			motor[middleright] = (joy_x)/slowdown;}
		// Standing still: (both abs(X) and abs(Y) are below the threshold)
		else{
			motor[twoleft]  = 0;
			motor[middleleft]  = 0;
			motor[tworight]  = 0;
			motor[middleright] = 0;}
		// Mobile Goal Lift motor
		motor[lift] = vexRT[Ch2]/slowdown;
		// DR4B Claw
		motor[claw] = vexRT[Ch3Xmtr2]/slowdown;
		// DR4B motors
		motor[dr4b1] = vexRT[Ch3Xmtr2]/slowdown;
		motor[dr4b2] = vexRT[Ch3Xmtr2]/slowdown;
		motor[dr4b3] = vexRT[Ch3Xmtr2]/slowdown;
		motor[dr4b4] = vexRT[Ch3Xmtr2]/slowdown;}}
